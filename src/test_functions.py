"""
Модуль с тестовыми функциями для численного интегрирования
"""

import numpy as np
from scipy.integrate import quad


def f1(x):
    """
    Гладкая функция: x^2

    Параметры:
    x - точка вычисления

    Возвращает:
    float - значение функции
    """
    return x ** 2


def f2(x):
    """
    Осциллирующая функция: sin(5x)

    Параметры:
    x - точка вычисления

    Возвращает:
    float - значение функции
    """
    return np.sin(5 * x)


def f3(x):
    """
    Функция с особенностью: sqrt(x)
    Имеет особенность в производной при x=0

    Параметры:
    x - точка вычисления

    Возвращает:
    float - значение функции
    """
    return np.sqrt(np.abs(x))


def f4(x):
    """
    Быстро меняющаяся функция: exp(-x^2)

    Параметры:
    x - точка вычисления

    Возвращает:
    float - значение функции
    """
    return np.exp(-x ** 2)


def f5(x):
    """
    Полином 3-й степени для проверки точности методов
    f(x) = x^3 - 2x^2 + x

    Параметры:
    x - точка вычисления

    Возвращает:
    float - значение функции
    """
    return x ** 3 - 2 * x ** 2 + x


# ==================== ТОЧНЫЕ ЗНАЧЕНИЯ ИНТЕГРАЛОВ ====================

def I1_exact(a, b):
    """
    Точное значение интеграла от x^2 на [a, b]

    Параметры:
    a, b - пределы интегрирования

    Возвращает:
    float - точное значение интеграла
    """
    return (b ** 3 - a ** 3) / 3


def I2_exact(a, b):
    """
    Точное значение интеграла от sin(5x) на [a, b]

    Параметры:
    a, b - пределы интегрирования

    Возвращает:
    float - точное значение интеграла
    """
    return (-np.cos(5 * b) + np.cos(5 * a)) / 5


def I3_exact(a, b):
    """
    Точное значение интеграла от sqrt(x) на [a, b]

    Параметры:
    a, b - пределы интегрирования

    Возвращает:
    float - точное значение интеграла
    """
    return (2 / 3) * (b ** (3 / 2) - a ** (3 / 2))


def I4_exact(a, b):
    """
    Точное значение интеграла от exp(-x^2) на [a, b]
    Вычисляется с помощью scipy.integrate.quad

    Параметры:
    a, b - пределы интегрирования

    Возвращает:
    float - точное значение интеграла
    """
    return quad(f4, a, b)[0]


def I5_exact(a, b):
    """
    Точное значение интеграла от полинома 3-й степени на [a, b]

    Параметры:
    a, b - пределы интегрирования

    Возвращает:
    float - точное значение интеграла
    """
    return (b ** 4 / 4 - 2 * b ** 3 / 3 + b ** 2 / 2) - \
        (a ** 4 / 4 - 2 * a ** 3 / 3 + a ** 2 / 2)


