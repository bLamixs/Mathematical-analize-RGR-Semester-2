"""
Модуль с реализацией квадратурных формул Ньютона-Котесса
"""

import numpy as np


def left_rectangle(f, a, b, n):
    """
    Метод левых прямоугольников

    Параметры:
    f - подынтегральная функция
    a, b - пределы интегрирования
    n - число разбиений

    Возвращает:
    float - приближённое значение интеграла
    """
    h = (b - a) / n
    x = np.linspace(a, b - h, n)
    return h * np.sum(f(x))


def right_rectangle(f, a, b, n):
    """
    Метод правых прямоугольников

    Параметры:
    f - подынтегральная функция
    a, b - пределы интегрирования
    n - число разбиений

    Возвращает:
    float - приближённое значение интеграла
    """
    h = (b - a) / n
    x = np.linspace(a + h, b, n)
    return h * np.sum(f(x))


def midpoint_rectangle(f, a, b, n):
    """
    Метод средних прямоугольников (наиболее точный из методов прямоугольников)

    Параметры:
    f - подынтегральная функция
    a, b - пределы интегрирования
    n - число разбиений

    Возвращает:
    float - приближённое значение интеграла
    """
    h = (b - a) / n
    x = np.linspace(a + h / 2, b - h / 2, n)
    return h * np.sum(f(x))


def trapezoidal(f, a, b, n):
    """
    Метод трапеций

    Параметры:
    f - подынтегральная функция
    a, b - пределы интегрирования
    n - число разбиений

    Возвращает:
    float - приближённое значение интеграла
    """
    h = (b - a) / n
    x = np.linspace(a, b, n + 1)
    y = f(x)
    return h * (0.5 * y[0] + 0.5 * y[-1] + np.sum(y[1:-1]))


def simpson(f, a, b, n):
    """
    Метод Симпсона (парабол)
    Требуется чётное число разбиений

    Параметры:
    f - подынтегральная функция
    a, b - пределы интегрирования
    n - число разбиений (будет увеличено до чётного при необходимости)

    Возвращает:
    float - приближённое значение интеграла
    """
    if n % 2 != 0:
        n += 1
    h = (b - a) / n
    x = np.linspace(a, b, n + 1)
    y = f(x)
    return h / 3 * (y[0] + y[-1] +
                    4 * np.sum(y[1:n:2]) +
                    2 * np.sum(y[2:n - 1:2]))


def three_eighths(f, a, b, n):
    """
    Метод 3/8 (трёх восьмых)
    Требуется число разбиений, кратное 3

    Параметры:
    f - подынтегральная функция
    a, b - пределы интегрирования
    n - число разбиений (будет увеличено до кратного 3 при необходимости)

    Возвращает:
    float - приближённое значение интеграла
    """
    if n % 3 != 0:
        n += 3 - (n % 3)
    h = (b - a) / n
    x = np.linspace(a, b, n + 1)
    y = f(x)
    return 3 * h / 8 * (y[0] + y[-1] +
                        3 * np.sum(y[1:n:3]) +
                        3 * np.sum(y[2:n:3]) +
                        2 * np.sum(y[3:n - 1:3]))


